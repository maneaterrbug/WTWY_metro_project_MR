SELECT *, 
CASE
WHEN TIME == '00:00:00' OR TIME BETWEEN '20:00:01' AND '23:59:59' THEN '00:00:00'
	WHEN TIME BETWEEN '00:00:01' AND '04:00:00' THEN '04:00:00'
	WHEN TIME BETWEEN '04:0:01' AND '08:00:00' THEN '08:00:00'
	WHEN TIME BETWEEN '08:00:01' AND '12:00:00' THEN '12:00:00'
	WHEN TIME BETWEEN '12:00:01' AND '16:00:00' THEN '16:00:00'
	WHEN TIME BETWEEN '16:00:01' AND '20:00:00' THEN '20:00:00'
END AS TIME_BUCKET
FROM mta_data;


